function [y1,y2,y3,y4,y5,a,b,c,d,e] = regress0_4(Y,X,x)

a=LRSp3(Y,X,0);
b=LRSp3(Y,X,1);
c=LRSp3(Y,X,2);
d=LRSp3(Y,X,3);
e=LRSp3(Y,X,4);

y1=a(1)+0*x+0*x.^2+x.^(3)*0+x.^(4)*0;
y2=b(1)+x*b(2)+x.^(2)*0+x.^(3)*0+x.^(4)*0;
y3=c(1)+x*c(2)+x.^(2)*c(3)+x.^(3)*0+x.^(4)*0;
y4=d(1)+x*d(2)+x.^(2)*d(3)+x.^(3)*d(4)+x.^(4)*0; 
y5=e(1)+x*e(2)+x.^(2)*e(3)+x.^(3)*e(4)+x.^(4)*e(5);